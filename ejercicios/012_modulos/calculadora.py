TIPO='CALCULADORA DE NÚMEROS ENTEROS'

def sumar(numero_1 : int, numero_2 : int) -> int:
    '''
    Suma dos números enteros
    
    numero_1 : Primer sumando
    numero_2 : Segundo sumando
    retorno : resultado de la suma
    '''
    return numero_1 + numero_2

def restar(numero_1, numero_2):
    return numero_1 - numero_2

def multiplicar(numero_1, numero_2):
    return numero_1 * numero_2

def dividir(numero_1, numero_2):
    return numero_1 / numero_2